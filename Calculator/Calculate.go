package calculator

func sum(a, b int) int {   //Addition Function

	return a + b
}

func sub(a, b int) int {    //Substraction function
	return a - b
}

func divide(a, b float64) float64 {  // division function

	return a / b
}

func mul(a, b float64) float64 {   // multiplying function
	return a * b

}
